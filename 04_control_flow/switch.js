//switch statement - if we need multiple condition checking then rather than using multiple if statement we use switch statements

const month = 5

switch(month)
{
    case 1:
        console.log("january")
        break;
    
    case 2:
        console.log("february")
        break;
    
    case 3:
        console.log("march")
        break;

    default:
        console.log("wrong input")
        break;
}